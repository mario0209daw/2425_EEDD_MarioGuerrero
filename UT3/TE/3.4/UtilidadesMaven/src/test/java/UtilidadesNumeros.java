public class UtilidadesNumeros {

    public static boolean esPar(int numero) {
        if (numero % 2 == 0) {
            return true;
        }
        return false;
    }

    public static boolean esImpar(int numero) {
        if (numero % 2 == 1) {
            return true;
        }
        return false;
    }
}
